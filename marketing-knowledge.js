/**
 * ฟังก์ชันค้นหาความรู้จากฐานข้อมูล
 */
export function searchKnowledge(keyword) {
  const results = [];
  
  for (const [key, value] of Object.entries(marketingKnowledge)) {
    if (key.toLowerCase().includes(keyword.toLowerCase()) ||
        value.title.toLowerCase().includes(keyword.toLowerCase()) ||
        value.description.toLowerCase().includes(keyword.toLowerCase())) {
      results.push({
        key,
        ...value
      });
    }
  }
  
  return results;
}

/**
 * ฟังก์ชันดึงเนื้อหาทั้งหมด
 */
export function getAllTopics() {
  return Object.keys(marketingKnowledge);
}

/**
 * ฟังก์ชันดึงเนื้อหาตามหัวข้อ
 */
export function getTopicContent(topic) {
  return marketingKnowledge[topic] || null;
}

